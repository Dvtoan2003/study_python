# Tạo một dictionary
my_dict = {'name': 'John', 'age': 30, 'city': 'New York'}
# Truy cập giá trị thông qua key
print(my_dict['name'])  
print(my_dict['age'])
print(my_dict['city'])

# Thêm một cặp key-value mới vào dictionary
my_dict['job'] = 'Engineer'
# Cập nhật giá trị của một key đã tồn tại
my_dict['age'] = 32
print(my_dict)

